//Function to output a console message and create an alert popup.
function sayHello() {
    console.log("hello world!");
    alert("hello world!");
}

// Call the function sayHello()
sayHello();

//A function called `sayHello()` is defined.  It has two statements:

// - `console.log` (to output a string to the console of the Browser Inspector) (Open with F12 on most browsers)
// - `alert()`  to open a popup with a string

// After a function is defined, it must be called (or “invoked”) in order to be executed.

// We can do with one simple line of JavaScript that contains the function name. 

// A function can also be called from the F12 inspector console window.